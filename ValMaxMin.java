public class ValMaxMin {
    public static void main(String[] args) {
        int intmax = Integer.MAX_VALUE;
        int intmin = Integer.MIN_VALUE;
        double doublemax = Double.MAX_VALUE;
        double doublemin = Double.MIN_VALUE;
        int charmax = Character.MAX_VALUE;
        int charmin = Character.MIN_VALUE;

        System.out.println("INT Valor maximo: " + intmax);
        System.out.println("INT Valor minimo: " + intmin);
        
        System.out.println("DOUBLE Valor maximo: " + doublemax);
        System.out.println("DOUBLE Valor minimo: " + doublemin);
        
        System.out.println("CHAR Valor maximo: " + charmax);
        System.out.println("CHAR Valor minimo: " + charmin);
    }
}
